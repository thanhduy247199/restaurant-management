package model;

/**
 *
 * @author Administrator
 */
public class USERS {
    private String email,password,role,avatar;

    public USERS(String email, String password, String role, String avatar) {
        this.email = email;
        this.password = password;
        this.role = role;
        this.avatar = avatar;
    }
    
    

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }
    
    
}
